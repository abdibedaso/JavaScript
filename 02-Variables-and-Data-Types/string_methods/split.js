// split(): The split method splits a string at a specified place.
let string = 'JavaScript Web Application Programming'
console.log(string.split()) // ["JavaScript Web Application Programming"]
console.log(string.split(' ')) // ["JavaScript", "Web", "Application", "Programming"]
let firstName = 'Abdi'
console.log(firstName.split()) // ["Abdi"]
console.log(firstName.split('')) // ["A", "b", "d", "i"]
let countries = 'USA, Sweden, Norway, Denmark, and Iceland'
console.log(countries.split(',')) // ["Finland", " Sweden", " Norway", " Denmark", " and Iceland"]
console.log(countries.split(', ')) //  ["Finland", "Sweden", "Norway", "Denmark", "and Iceland"]