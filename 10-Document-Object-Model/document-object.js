var b = document.cookie; // HTML DOM cookie Property - Get the cookies associated with the current document
var c = document.domain; // HTML domain Property - Get the domain name of the server that loaded the document
var d = document.forms.length; // HTML DOM forms Collection - Find out how many <form> elements there are in the document
