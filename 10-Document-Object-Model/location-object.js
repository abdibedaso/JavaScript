var b = location.hostname; // Location hostname Property - Return the hostname of the current URL
var c = location.href; // Location href Property - Return the entire URL (of the current page)
var d = "Port: " + location.port; // Location port Property - Return the port number of the current URL
var e = location.pathname; // Location pathname Property - Return the path name of the current URL
var f = location.protocol; //Location protocol Property - Return the protocol of the current URL
var g = location.search; // Location search Property - Return the querystring part of a URL